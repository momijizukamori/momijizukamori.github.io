cy.on('tap', 'node', function(evt){
  var node = evt.target;
  console.log( 'tapped ' + node.id() + ' at ' + JSON.stringify(node.position()));
  var noteLevel = node.data('note') != null ? (node.data('note') + 1) % 4 : 1;
  console.log(noteLevel);
  node.data('note', noteLevel);
  // node.addClass('selected');
  switch (noteLevel) {
  	case 0: 
  		updateBombs(node.id())
  		break;
  	case 1:
  		distance1(node);
  		break;
  	case 2:
  		distance2(node);
  		break;
  	case 3:
  		distance3(node);
  		break;
  }


});

function distance1(node) {
var bombs = node.neighborhood();
updateBombs(node.id(), bombs);
}

function distance2(node) {
var first = node.neighborhood();
var second = first.neighborhood();
var bombs = second.difference(first.union(node));
updateBombs(node.id(), bombs);

}

function distance3(node) {
var first = node.neighborhood();
var second = first.neighborhood();
var third = second.neighborhood();
var bombs = third.difference(second.union(first).union(node));
updateBombs(node.id(), bombs);
}

function updateBombs(id, bombs) {
	Object.values(bombSet).forEach(function(bombGroup) {bombGroup.removeClass('bomb')})
	if (bombs == null) {
		delete bombSet[id];
	} else {
		bombSet[id] = bombs;
	}
	var targets = null;
	Object.values(bombSet).forEach(function(bombGroup) {
		if (targets == null) {
			targets = bombGroup
		} else {
			targets = targets.intersection(bombGroup);
		}

	});
	if (targets) {targets.addClass('bomb')};
}